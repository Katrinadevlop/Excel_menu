# PowerShell скрипт для коммита изменений
Write-Host "Коммит исправлений в модуле извлечения блюд..." -ForegroundColor Green
Write-Host ""

Write-Host "1. Проверяем статус git..." -ForegroundColor Yellow
git status

Write-Host ""
Write-Host "2. Добавляем все измененные файлы..." -ForegroundColor Yellow
git add .

Write-Host ""
Write-Host "3. Делаем коммит..." -ForegroundColor Yellow

$commitMessage = @"
Исправлена проблема с извлечением блюд из правых столбцов Excel

- Расширен поиск данных с одного столбца E на все столбцы D,E,F,G,H...
- Исправлена логика извлечения салатов после заголовка 'САЛАТЫ и ХОЛОДНЫЕ ЗАКУСКИ'
- Добавлена подробная отладочная информация с указанием колонки и строки
- Теперь категории 'первое', 'мясо', 'курица', 'рыба', 'гарнир' извлекаются корректно
- Столбец G в бракеражном журнале больше не остается пустым

Файлы:
- brokerage_journal.py - основные исправления
- quick_test.py - тестовый скрипт
- run_test.bat - батник для проверки
- ИСПРАВЛЕНИЯ_ГОТОВО.txt - краткое описание
- ПРОВЕРКА_ИСПРАВЛЕНИЙ.md - подробная инструкция
- РЕЗЮМЕ_РАБОТ.md - полное резюме работ
- СТАРТ_ПРОВЕРКИ.txt - инструкция для пользователя
"@

git commit -m $commitMessage

Write-Host ""
Write-Host "Коммит завершен!" -ForegroundColor Green
Read-Host "Нажмите Enter для продолжения"
